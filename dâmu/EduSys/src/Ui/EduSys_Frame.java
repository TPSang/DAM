    private void openThongKe(int index) {
        //mở Dialog thống kê
        if (Auth.isLogin()) {
            if (index == 3 && !Auth.isManager()) {
                MsgBox.warning(this, "Bạn Không Có Quyền Truy Cập Tab Doanh Thu !");
            } else {
                ThongKe_Dialog tk = new ThongKe_Dialog(this, true);
                tk.selectTab(index);
                tk.setVisible(true);

            }
        } else {
            MsgBox.warning(this, "Vui Lòng Đăng Nhập Vào Hệ Thống !");
        }
    }

