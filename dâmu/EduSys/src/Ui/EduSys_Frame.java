    private void init() {
        //chuyển frame ra giữa màn hình
        this.setLocationRelativeTo(null);
        //gọi hàm tạo icon tiêu đề
        this.setIconImage(XImage.getAppIcon());
        //mở các Dialog tương ứng khi chạy
        new ChaoMung_Dialog(this, true).setVisible(true);
        new DangNhap_Dialog(this, true).setVisible(true);
        if (!Auth.isLogin()) {
            System.exit(0);
        }
    }

    private void dongHo() {
        //lấy thời gian hệ thống đưa lên lbl_Dongho
        new Timer(1000, new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                Date now = new Date();
                SimpleDateFormat SDF = new SimpleDateFormat("HH:mm:ss a");
                String DH = SDF.format(now);
                lbl_Dongho.setText(DH);
            }
        }).start();
    }

